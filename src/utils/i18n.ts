// Detect browser/device language
export function detectLanguage(): LanguageCode {
  const browserLang = navigator.language || (navigator as any).userLanguage || 'en';
  const langCode = browserLang.split('-')[0].toLowerCase();

  // Map to supported languages
  const supportedCodes: LanguageCode[] = ['en', 'es', 'fr', 'de', 'nl', 'zh', 'ja', 'ru', 'pt', 'ar'];

  if (supportedCodes.includes(langCode as LanguageCode)) {
    return langCode as LanguageCode;
  }

  // Default to English
  return 'en';
}

// Get translation
export function t(key: string, lang: LanguageCode = 'en'): string {
  return UI_TRANSLATIONS[lang]?.[key] || UI_TRANSLATIONS['en']?.[key] || key;
}

// Get language info
export function getLanguageInfo(code: LanguageCode): Language | undefined {
  return LANGUAGES.find(l => l.code === code);
}

// Save language preference
export function saveLanguagePreference(code: LanguageCode): void {
  localStorage.setItem('nebula-maze-language', code);
}

// Load language preference
export function loadLanguagePreference(): LanguageCode {
  const saved = localStorage.getItem('nebula-maze-language');
  if (saved && LANGUAGES.some(l => l.code === saved)) {
    return saved as LanguageCode;
  }
  return detectLanguage();
}
